package aula13;

public class OperadoresRelacionais {
    public static void main(String[] args) {
        int result1 = 1;
        int result2 = 2;

        System.out.println("result1 == result2: " + (result1 == result2));
        System.out.println("result1 != result2: " + (result1 != result2));
        System.out.println("result1 > result2: " + (result1 > result2));
        System.out.println("result1 >= result2: " + (result1 >= result2));
        System.out.println("result1 < result2: " + (result1 < result2));
        System.out.println("result1 <= result2: " + (result1 <= result2));
    }
}