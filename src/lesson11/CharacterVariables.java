package lesson11;

public class CharacterVariables {
    public static void main(String[] args) {
        char o = 111;
        char i = 105;
        char quest = 0X003F;

        System.out.println(o);
        System.out.println(o + i + quest);
        System.out.println("" + o + i + quest);
        System.out.println(o + " " + i + quest);
    }
}
